# AG Grid report generator
generate_aggrid_report() {
  cat > "$REPORT_DIR/${REPORT_NAME}.js" << 'EOF'
// @ts-check
/**
 * {REPORT_NAME} - AG Grid Report
 * Generated by ASDM Report Development Skill
 */

const {REPORT_NAME} = (function() {
  'use strict';

  // Default column definitions
  const defaultColumns = [
    { field: 'id', headerName: 'ID', width: 90, sortable: true, filter: true },
    { field: 'name', headerName: 'Name', width: 150, sortable: true, filter: true },
    { field: 'value', headerName: 'Value', width: 120, sortable: true, filter: 'agNumberColumnFilter' },
    { field: 'date', headerName: 'Date', width: 150, sortable: true }
  ];

  // Sample data - replace with actual data source
  const defaultData = [
    { id: 1, name: 'Item 1', value: 100, date: '2024-01-01' },
    { id: 2, name: 'Item 2', value: 200, date: '2024-01-02' },
    { id: 3, name: 'Item 3', value: 150, date: '2024-01-03' }
  ];

  // Grid options
  let gridOptions = null;

  /**
   * Initialize the grid
   * @param {string} gridDivId - Grid DIV element ID
   * @param {Array} data - Row data (optional)
   * @param {Array} columns - Column definitions (optional)
   */
  function init(gridDivId, data = defaultData, columns = defaultColumns) {
    gridOptions = {
      columnDefs: columns,
      rowData: data,
      defaultColDef: {
        resizable: true,
        sortable: true,
        filter: true
      },
      pagination: true,
      paginationPageSize: 20,
      rowSelection: 'multiple',
      animateRows: true
    };

    const gridDiv = document.getElementById(gridDivId);
    if (!gridDiv) {
      console.error('Grid DIV not found:', gridDivId);
      return null;
    }

    return new agGrid.Grid(gridDiv, gridOptions);
  }

  /**
   * Update grid data
   * @param {Array} newData - New row data
   */
  function updateData(newData) {
    if (gridOptions) {
      gridOptions.api.setRowData(newData);
    }
  }

  /**
   * Export to CSV
   */
  function exportCSV() {
    if (gridOptions) {
      gridOptions.api.exportDataAsCsv();
    }
  }

  /**
   * Get selected rows
   * @returns {Array} Selected row data
   */
  function getSelectedRows() {
    if (gridOptions) {
      return gridOptions.api.getSelectedRows();
    }
    return [];
  }

  return {
    init,
    updateData,
    exportCSV,
    getSelectedRows
  };
})();
EOF

  sed -i "s/{REPORT_NAME}/$REPORT_NAME/g" "$REPORT_DIR/${REPORT_NAME}.js"

  cat > "$REPORT_DIR/${REPORT_NAME}.html" << EOF
<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>$REPORT_NAME - AG Grid</title>
  <link rel="stylesheet" href="https://cdn.jsdelivr.net/npm/ag-grid-community@30.0.0/styles/ag-grid.css">
  <link rel="stylesheet" href="https://cdn.jsdelivr.net/npm/ag-grid-community@30.0.0/styles/ag-theme-alpine.css">
