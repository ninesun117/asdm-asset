#!/bin/bash

# Report Generator Script
# Generates report templates for various frameworks and types

set -e

# Color output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m'

# Default values
REPORT_NAME=""
REPORT_TYPE="table"
FRAMEWORK="chartjs"
OUTPUT_DIR="."

# Usage function
usage() {
  echo "Usage: $0 <report-name> [report-type] [framework] [output-dir]"
  echo ""
  echo "Arguments:"
  echo "  report-name   Name of the report (PascalCase, e.g., SalesReport)"
  echo "  report-type   Type of report: table, chart, pdf, excel, dashboard (default: table)"
  echo "  framework     Framework to use (default: chartjs)"
  echo "                Tables: ag-grid, datatables"
  echo "                Charts: chartjs, echarts"
  echo "                PDF: itext, wkhtmltopdf, jasperreports"
  echo "                Excel: poi, exceljs"
  echo "  output-dir    Output directory (default: current directory)"
  echo ""
  echo "Examples:"
  echo "  $0 SalesReport table chartjs ./reports"
  echo "  $0 UserActivityReport chart echarts"
  echo "  $0 FinancialReport pdf jasperreports ./output"
  exit 1
}

# Parse arguments
if [ $# -lt 1 ]; then
  usage
fi

REPORT_NAME="$1"
REPORT_TYPE="${2:-table}"
FRAMEWORK="${3:-chartjs}"
OUTPUT_DIR="${4:-.}"

# Validate report name
if [[ ! "$REPORT_NAME" =~ ^[A-Z][a-zA-Z0-9]*$ ]]; then
  echo -e "${RED}Error: Report name must be PascalCase (e.g., SalesReport)${NC}"
  exit 1
fi

# Validate report type
case "$REPORT_TYPE" in
  table|chart|pdf|excel|dashboard) ;;
  *)
    echo -e "${RED}Error: Invalid report type: $REPORT_TYPE${NC}"
    echo "Valid types: table, chart, pdf, excel, dashboard"
    exit 1
    ;;
esac

# Create output directory
REPORT_DIR="$OUTPUT_DIR/$REPORT_NAME"
mkdir -p "$REPORT_DIR"

echo -e "${GREEN}Generating report: $REPORT_NAME${NC}"
echo "  Type: $REPORT_TYPE"
echo "  Framework: $FRAMEWORK"
echo "  Output: $REPORT_DIR"

# Generate based on framework
case "$FRAMEWORK" in
  chartjs)
    generate_chartjs_report
    ;;
  echarts)
    generate_echarts_report
    ;;
  ag-grid)
    generate_aggrid_report
    ;;
  datatables)
    generate_datatables_report
    ;;
  itext|wkhtmltopdf|jasperreports)
    generate_pdf_report
    ;;
  poi|exceljs)
    generate_excel_report
    ;;
  *)
    echo -e "${RED}Error: Unknown framework: $FRAMEWORK${NC}"
    exit 1
    ;;
esac

echo -e "${GREEN}Report generated successfully!${NC}"
echo "Files created in: $REPORT_DIR"

# Chart.js report generator
generate_chartjs_report() {
  cat > "$REPORT_DIR/${REPORT_NAME}.js" << 'EOF'
// @ts-check
/**
 * {REPORT_NAME} - Chart.js Report
 * Generated by ASDM Report Development Skill
 */

const {REPORT_NAME} = (function() {
  'use strict';

  // Default configuration
  const config = {
    chartType: 'bar',
    responsive: true,
    maintainAspectRatio: false,
    plugins: {
      legend: { display: true },
      tooltip: { enabled: true }
    }
  };

  // Sample data - replace with actual data source
  const defaultData = {
    labels: [],
    datasets: [{
      label: '{REPORT_NAME} Data',
      data: [],
      backgroundColor: 'rgba(54, 162, 235, 0.5)',
      borderColor: 'rgba(54, 162, 235, 1)',
      borderWidth: 1
    }]
  };

  /**
   * Initialize the chart
   * @param {string} canvasId - Canvas element ID
   * @param {Object} data - Chart data (optional)
   */
  function init(canvasId, data = defaultData) {
    const ctx = document.getElementById(canvasId);
    if (!ctx) {
      console.error('Canvas element not found:', canvasId);
      return null;
    }

    return new Chart(ctx, {
      type: config.chartType,
      data: data,
      options: config
    });
  }

  /**
   * Update chart data
   * @param {Object} chart - Chart instance
   * @param {Object} newData - New data to display
   */
  function updateData(chart, newData) {
    if (chart) {
      chart.data = newData;
      chart.update();
    }
  }

  /**
   * Change chart type
   * @param {Object} chart - Chart instance
   * @param {string} type - New chart type
   */
  function changeType(chart, type) {
    if (chart) {
      chart.config.type = type;
      chart.update();
    }
  }

  return {
    config,
    init,
    updateData,
    changeType
  };
})();
EOF

  sed -i "s/{REPORT_NAME}/$REPORT_NAME/g" "$REPORT_DIR/${REPORT_NAME}.js"

  cat > "$REPORT_DIR/${REPORT_NAME}.html" << EOF
<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>$REPORT_NAME - Chart.js</title>
  <script src="https://cdn.jsdelivr.net/npm/chart.js"></script>
</head>
<body>
  <div class="report-container">
    <h1>$REPORT_NAME</h1>
    <canvas id="chart"></canvas>
  </div>
  <script src="${REPORT_NAME}.js"></script>
  <script>
    const chart = ${REPORT_NAME}.init('chart');
  </script>
</body>
</html>
EOF
}

# ECharts report generator
generate_echarts_report() {
  cat > "$REPORT_DIR/${REPORT_NAME}.js" << 'EOF'
// @ts-check
/**
 * {REPORT_NAME} - ECharts Report
 * Generated by ASDM Report Development Skill
 */

const {REPORT_NAME} = (function() {
  'use strict';

  // Default configuration
  const config = {
    responsive: true,
    maintainAspectRatio: false,
    title: {
      text: '{REPORT_NAME}'
    },
    tooltip: { trigger: 'axis' },
    legend: { data: [] },
    xAxis: { type: 'category', data: [] },
    yAxis: { type: 'value' },
    series: []
  };

  // Sample data - replace with actual data source
  const defaultData = {
    legend: ['Sales'],
    xAxisData: ['Jan', 'Feb', 'Mar', 'Apr', 'May'],
    seriesData: [
      { name: 'Sales', type: 'bar', data: [120, 200, 150, 80, 70] }
    ]
  };

  /**
   * Initialize the chart
   * @param {string} domId - DOM element ID
   * @param {Object} data - Chart data (optional)
   */
  function init(domId, data = defaultData) {
    const dom = document.getElementById(domId);
    if (!dom) {
      console.error('DOM element not found:', domId);
      return null;
    }

    const chart = echarts.init(dom);
    const options = JSON.parse(JSON.stringify(config));

    options.legend.data = data.legend;
    options.xAxis.data = data.xAxisData;
    options.series = data.seriesData;

    chart.setOption(options);
    return chart;
  }

  /**
   * Update chart data
   * @param {Object} chart - ECharts instance
   * @param {Object} newData - New data to display
   */
  function updateData(chart, newData) {
    if (chart) {
      chart.setOption({
        legend: { data: newData.legend },
        xAxis: { data: newData.xAxisData },
        series: newData.seriesData
      });
    }
  }

  return {
    config,
    init,
    updateData
  };
})();
EOF

