# DataTables report generator
generate_datatables_report() {
  cat > "$REPORT_DIR/${REPORT_NAME}.js" << 'EOF'
// @ts-check
/**
 * {REPORT_NAME} - DataTables Report
 * Generated by ASDM Report Development Skill
 */

const {REPORT_NAME} = (function() {
  'use strict';

  // Sample data - replace with actual data source
  const defaultData = [
    [1, 'Item 1', 100, '2024-01-01'],
    [2, 'Item 2', 200, '2024-01-02'],
    [3, 'Item 3', 150, '2024-01-03']
  ];

  // Default columns
  const defaultColumns = [
    { title: 'ID' },
    { title: 'Name' },
    { title: 'Value' },
    { title: 'Date' }
  ];

  let table = null;

  /**
   * Initialize DataTable
   * @param {string} tableId - Table element ID
   * @param {Array} data - Row data (optional)
   * @param {Array} columns - Column definitions (optional)
   */
  function init(tableId, data = defaultData, columns = defaultColumns) {
    table = $(tableId).DataTable({
      data: data,
      columns: columns,
      paging: true,
      searching: true,
      ordering: true,
      info: true,
      responsive: true,
      dom: 'Bfrtip',
      buttons: [
        'copyHtml5',
        'excelHtml5',
        'pdfHtml5',
        'print'
      ]
    });
    return table;
  }

  /**
   * Update table data
   * @param {Array} newData - New row data
   */
  function updateData(newData) {
    if (table) {
      table.clear().rows.add(newData).draw();
    }
  }

  /**
   * Add new row
   * @param {Array} rowData - New row data
   */
  function addRow(rowData) {
    if (table) {
      table.row.add(rowData).draw();
    }
  }

  /**
   * Search table
   * @param {string} query - Search query
   */
  function search(query) {
    if (table) {
      table.search(query).draw();
    }
  }

  return {
    init,
    updateData,
    addRow,
    search
  };
})();
EOF

  sed -i "s/{REPORT_NAME}/$REPORT_NAME/g" "$REPORT_DIR/${REPORT_NAME}.js"

  cat > "$REPORT_DIR/${REPORT_NAME}.html" << EOF
